package example2;

public class Result {
	private String name;
	private Integer lenght;
	public Result(String name, Integer lenght) {
		this.name = name;
		this.lenght = lenght;
				
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getLenght() {
		return lenght;
	}
	public void setLenght(Integer lenght) {
		this.lenght = lenght;
	}
	
}
